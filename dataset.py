import os
import json
import csv
from pathlib import Path

import numpy as np
import torch
from torch.utils.data import Dataset
from PIL import Image
import torchvision.transforms as T
import torchvision.transforms.functional as TF
import albumentations as A
from albumentations.pytorch import ToTensorV2


# ── Assembly state mapping ────────────────────────────────────────────────────
# Mapped from the IKEA ASM atomic action taxonomy (102 classes) to 6 coarse states.
# Adjust this mapping based on which furniture categories you include.
ATOMIC_TO_COARSE_STATE = {
    "NA": 0,  # not_started
    # ... populate from atomic_actions_labels.csv after download
}


class IKEAAsmDataset(Dataset):
    """
    Loads video clips from the IKEA ASM dataset with action state labels
    and bounding box annotations.

    Args:
        root:       Path to ikea_asm_dataset_public/.
        split:      "train" | "val" | "test".
        T:          Frames per clip (uniformly sampled from each video segment).
        img_size:   Resize each frame to (H, W).
        max_objects: Maximum tracked objects per frame.
        augment:    Apply training augmentations (colour jitter, horizontal flip).
    """

    MEAN = [0.485, 0.456, 0.406]
    STD  = [0.229, 0.224, 0.225]

    def __init__(
        self,
        root: str,
        split: str = "train",
        T: int = 8,
        img_size: tuple[int, int] = (224, 224),
        max_objects: int = 4,
        augment: bool = True,
    ):
        self.root = Path(root)
        self.split = split
        self.T = T
        self.img_size = img_size
        self.max_objects = max_objects
        self.augment = augment and (split == "train")

        self.clips = self._load_clip_index()

        # Albumentations pipeline (bbox-aware augmentation)
        if self.augment:
            self.aug = A.Compose(
                [
                    A.HorizontalFlip(p=0.5),
                    A.ColorJitter(
                        brightness=0.3, contrast=0.3, saturation=0.2, hue=0.05, p=0.8
                    ),
                    A.GaussianBlur(blur_limit=(3, 5), p=0.2),
                    A.RandomBrightnessContrast(p=0.3),
                    A.CoarseDropout(
                        max_holes=4, max_height=30, max_width=30, p=0.3
                    ),  # simulates steam / arm occlusion
                    A.Normalize(mean=self.MEAN, std=self.STD),
                    ToTensorV2(),
                ],
                bbox_params=A.BboxParams(
                    format="coco",           # (x_min, y_min, w, h) in pixels
                    label_fields=["obj_ids"],
                    min_visibility=0.1,
                ),
            )
        else:
            self.aug = A.Compose(
                [
                    A.Normalize(mean=self.MEAN, std=self.STD),
                    ToTensorV2(),
                ],
                bbox_params=A.BboxParams(
                    format="coco",
                    label_fields=["obj_ids"],
                    min_visibility=0.1,
                ),
            )

    def _load_clip_index(self) -> list[dict]:
        """
        Reads the pre-processed clip index JSON.
        Expected format (generated by data/preprocess_ikea.py):
            [
                {
                    "video_id": "...",
                    "split": "train",
                    "state": 2,
                    "instruction": "Attach the ...",
                    "frame_paths": ["/path/to/frame_0001.jpg", ...],
                    "bboxes": [
                        // per frame, list of {obj_id, bbox: [cx, cy, w, h] normalised}
                    ]
                },
                ...
            ]
        """
        index_path = self.root / "clip_index.json"
        if not index_path.exists():
            raise FileNotFoundError(
                f"clip_index.json not found at {index_path}. "
                "Run data/preprocess_ikea.py first, or use SyntheticAssemblyDataset."
            )
        with open(index_path) as f:
            all_clips = json.load(f)
        return [c for c in all_clips if c["split"] == self.split]

    def __len__(self) -> int:
        return len(self.clips)

    def __getitem__(self, idx: int) -> dict:
        clip_meta = self.clips[idx]
        frame_paths = clip_meta["frame_paths"]
        state = clip_meta["state"]
        instruction = clip_meta["instruction"]

        # Uniform temporal sampling: pick T evenly spaced frames
        indices = np.linspace(0, len(frame_paths) - 1, self.T, dtype=int)

        H, W = self.img_size
        frames = []
        boxes_seq  = np.zeros((self.T, self.max_objects, 4), dtype=np.float32)
        box_mask   = np.zeros((self.T, self.max_objects), dtype=bool)

        for t, fi in enumerate(indices):
            img = Image.open(frame_paths[fi]).convert("RGB")
            img = img.resize((W, H), Image.BILINEAR)
            img_np = np.array(img)

            frame_bboxes = clip_meta["bboxes"][fi] if fi < len(clip_meta["bboxes"]) else []

            # Convert normalised (cx, cy, w, h) → COCO pixel (x_min, y_min, w_px, h_px)
            coco_bboxes, obj_ids = [], []
            for ann in frame_bboxes[:self.max_objects]:
                cx, cy, bw, bh = ann["bbox"]
                x_min = (cx - bw / 2) * W
                y_min = (cy - bh / 2) * H
                w_px  = bw * W
                h_px  = bh * H
                coco_bboxes.append([x_min, y_min, w_px, h_px])
                obj_ids.append(ann["obj_id"])

            result = self.aug(
                image=img_np, bboxes=coco_bboxes, obj_ids=obj_ids
            )

            frames.append(result["image"])  # (3, H, W) tensor

            # Convert back to normalised (cx, cy, w, h)
            for obj_i, bbox_pix in enumerate(result["bboxes"]):
                if obj_i >= self.max_objects:
                    break
                xm, ym, wp, hp = bbox_pix
                boxes_seq[t, obj_i] = [
                    (xm + wp / 2) / W,
                    (ym + hp / 2) / H,
                    wp / W,
                    hp / H,
                ]
                box_mask[t, obj_i] = True

        clip = torch.stack(frames, dim=0)  # (T, 3, H, W)

        return {
            "clip":        clip,
            "instruction": instruction,
            "state_label": torch.tensor(state, dtype=torch.long),
            "boxes":       torch.tensor(boxes_seq),
            "box_mask":    torch.tensor(box_mask),
        }
